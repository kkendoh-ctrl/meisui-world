// テーブル行ヘルパー
function tableRow(cells, isHeader) {
    return new TableRow({
        children: cells.map((text, i) => new TableCell({
            borders,
            width: { size: ({2:[2400,6626], 3:[1800,3613,3613], 4:[1000,4000,2000,2026]}[cells.length] || [2200,2200,2200,2426])[i], type: WidthType.DXA },
            margins: cellMargins,
            shading: isHeader ? { fill: "1B4F72", type: ShadingType.CLEAR } : undefined,
            children: [new Paragraph({
                children: [new TextRun({
                    text, bold: isHeader, color: isHeader ? "FFFFFF" : "333333",
                    font: "BIZ UDGothic", size: isHeader ? 22 : 20
                })]
            })]
        }))
    });
}

// 見出しヘルパー
function h1(text) {
    return new Paragraph({
        heading: HeadingLevel.HEADING_1,
        spacing: { before: 400, after: 200 },
        children: [new TextRun({ text, bold: true, font: "BIZ UDGothic", size: 36, color: "1B4F72" })]
    });
}
function h2(text) {
    return new Paragraph({
        heading: HeadingLevel.HEADING_2,
        spacing: { before: 300, after: 150 },
        children: [new TextRun({ text, bold: true, font: "BIZ UDGothic", size: 28, color: "2E75B6" })]
    });
}
function h3(text) {
    return new Paragraph({
        heading: HeadingLevel.HEADING_3,
        spacing: { before: 200, after: 100 },
        children: [new TextRun({ text, bold: true, font: "BIZ UDGothic", size: 24, color: "4A90D9" })]
    });
}
function p(text, opts = {}) {
    return new Paragraph({
        spacing: { after: 120 },
        children: [new TextRun({
            text, font: "BIZ UDGothic", size: 21,
            color: opts.color || "333333",
            bold: opts.bold || false,
            italics: opts.italic || false
        })]
    });
}
function pMulti(runs) {
    return new Paragraph({
        spacing: { after: 120 },
        children: runs.map(r => new TextRun({
            text: r.text, font: "BIZ UDGothic", size: 21,
            color: r.color || "333333", bold: r.bold || false
        }))
    });
}

